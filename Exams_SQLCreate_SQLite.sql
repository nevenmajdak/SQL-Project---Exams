-- Create tables with column names
CREATE TABLE DEPARTMENTS (
	ID_DEPARTMENT INTEGER NOT NULL UNIQUE,
	DEPARTMENT_NAME TEXT
);

CREATE TABLE COURSES (
	ID_COURSE INTEGER NOT NULL UNIQUE,
	COURSE_NAME TEXT,
	CODE TEXT,
	REQUIRED_OPTIONAL TEXT,
	ID_DEPARTMENT INTEGER,
	ID_PROFESSOR INTEGER,
	ECTS INTEGER,
	YEAR INTEGER
);

CREATE TABLE REGISTRATIONS (
	ID_REGISTRATION INTEGER NOT NULL UNIQUE,
	ID_STUDENT TEXT,
	ID_COURSE INTEGER,
	ATTEMPT INTEGER,
	ID_EXAM_SESSION INTEGER
);

CREATE TABLE PROFESSORS (
	ID_PROFESSOR INTEGER NOT NULL UNIQUE,
	TITLE TEXT,
	FIRST_NAME TEXT,
	LAST_NAME TEXT,
	ID_DEPARTMENT INTEGER
);

CREATE TABLE STUDENTS (
	ID_STUDENT TEXT NOT NULL UNIQUE,
	FIRST_NAME TEXT,
	LAST_NAME TEXT,
	YEAR INTEGER
);

CREATE TABLE EXAM_SESSIONS (
	ID_SESSION INTEGER NOT NULL UNIQUE,
	ID_COURSE INTEGER,
	SESSION_DATE TEXT,
	SESSION_TIME TEXT,
	SESSION_ROOM TEXT
);


